package whileSwitch.daysPrint;

public class Days {
    public void dayPrint(int day){
        switch (day){
            case 1:
                System.out.println("Dushanba");
                break;
            case 2:
                System.out.println("Sewanba");
                break;
            case 3:
                System.out.println("Chorshanba");
                break;
            case 4:
                System.out.println("Paywanba");
                break;
            case 5:
                System.out.println("Juma");
                break;
            case 6:
                System.out.println("Shanba");
                break;
            case 7:
                System.out.println("Yakwanba");
                break;
            default:
                System.out.println("Notogri kun");
        }

    }
}
